/**
 * Confidence badge for AI-generated content.
 * Levels: high (green), medium (amber), low (red)
 * Rule-based heuristic: sections with direct document extraction = high,
 * inferred/estimated data = medium, speculative data = low.
 */
export function ConfidenceBadge({ level = "medium", className = "" }) {
  const config = {
    high: { label: "High confidence", bg: "bg-emerald-500/10", text: "text-emerald-400", border: "border-emerald-500/20" },
    medium: { label: "Medium confidence", bg: "bg-amber-500/10", text: "text-amber-400", border: "border-amber-500/20" },
    low: { label: "Low confidence", bg: "bg-red-500/10", text: "text-red-400", border: "border-red-500/20" },
  };
  const c = config[level] || config.medium;

  return (
    <span
      className={`inline-flex items-center gap-1 px-2 py-0.5 rounded text-[10px] font-semibold uppercase tracking-wider border ${c.bg} ${c.text} ${c.border} ${className}`}
      title={`AI confidence: ${level}. This output was generated by AI and should be verified.`}
    >
      <svg className="w-2.5 h-2.5" fill="currentColor" viewBox="0 0 20 20">
        <path fillRule="evenodd" d="M18 10a8 8 0 1 1-16 0 8 8 0 0 1 16 0Zm-7-4a1 1 0 1 1-2 0 1 1 0 0 1 2 0ZM9 9a.75.75 0 0 0 0 1.5h.253a.25.25 0 0 1 .244.304l-.459 2.066A1.75 1.75 0 0 0 10.747 15H11a.75.75 0 0 0 0-1.5h-.253a.25.25 0 0 1-.244-.304l.459-2.066A1.75 1.75 0 0 0 9.253 9H9Z" clipRule="evenodd" />
      </svg>
      {c.label}
    </span>
  );
}

