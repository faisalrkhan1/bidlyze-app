/**
 * Assigns a heuristic confidence level to each analysis section.
 * High = directly extracted from document text
 * Medium = analyzed/interpreted from document
 * Low = inferred/speculated beyond document content
 */
export function getSectionConfidence(sectionName) {
  const levels = {
    // High confidence — extracted directly from document
    summary: "high",
    requirements: "high",
    keyDates: "high",
    evaluationCriteria: "high",
    financialRequirements: "high",

    // Medium confidence — analyzed/interpreted
    complianceAnalysis: "medium",
    riskRadar: "medium",
    bidScore: "medium",

    // Low confidence — speculative/inferred
    winProbability: "low",
    competitorIntelligence: "low",
    pricingAdvisor: "low",
  };
  return levels[sectionName] || "medium";
}
